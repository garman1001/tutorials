# This python teaches you about comments.

print("Comments are fun.")

# Comments are ignored by the interpreter,
# so developers can write information about the
# file. Comments do not run as code.

# To write a comment, create a new line, type
# a hashtag, and write anything after it.

# This is a comment.

# There is another way to comment in Python. Use
# 3 single quotes together. Anything inside those
# and another 3 single quotes counts as a comment.

'''
This is another comment.
Comments inside these can be on
multiple lines.

Cool, huh?
'''

# You can also comment out code if you don't want
# it to run.

