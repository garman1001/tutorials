# Initializes a list (var = [e1, e2, etc...])
friends = ["Kevin", "Karen", "Jim", "Oscar", "Toby"]

print(friends)

# Prints an element in a specified spot, 0 being
# the first spot
print(friends[0])
print(friends[1])
print(friends[2])

# Prints all elements from spot 2 onward
print(friends[2:])

# Changes an element in a certain spot in the list to
# another element
friends[2] = "Dwight"

print(friends[2])