# A while loop is a type of loop that
# says 
# while <condition>:
#   do this code

# Initialize a variable
i = 1

# While i is less than or equal to 10,
# print i. Then, add 1 to i.
# This will print numbers 1-10.
while i <= 10:
    print(i)
    i += 1

# The loop will continue to run while the
# condition remains true. However,
# when the condition in the loop becomes false,
# the loop stops and the code moves on.

# Prints "Done with loop" when the loop finished.
print("Done with loop.")