/*
* Common Headers
*
* #include <stdio.h>
* #include <stdlib.h>
* #include <math.h>
*/

/*
* Data Types
*
* char         - Character
* char[]       - String
* int          - Integer
* short        - 2 byte number
* long         - 4 byte number
* float        - 32 bit number between 10^-37 and 10^37
* double       - 64 bit number with 53 bit precision
* long double  - 80 bit number with 64 bit precision
*/

/*
* printf references
*
* %c       - Character
* %d       - Integer (base 10)
* %e       - Exponential floating-point
* %f       - Floating-point
* %i       - Integer (base 10)
* %o       - Octal (base 8)
* %s       - String
* %u       - Unsigned decimal (integer) number
* %x       - Hexadecimal (base 16)
* %%       - Print a percent sign
* \%       - Print a percent sign
*/

/*
* Tips:
*
* Strings:
*   - If declaring a string variable before defining it, use `char var[length in characters];`.
*
*/